@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        form_data = {key: value.data for (key, value) in form._fields.items() if key not in ["submit", "csrf_token"]}
        # print(form_data)
        if User.query.filter_by(email=form_data["email"]).first():
            flash("You've already signed up with that email, login instead.")
            return redirect(url_for("login"))
        form_data["password"] = generate_password_hash(form_data["password"], "pbkdf2:sha256", salt_length=8)
        new_user = User(**form_data)
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for("get_all_posts"))
    return render_template("register.html", form=form)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        form_data = {key: value.data for (key, value) in form._fields.items() if key not in ["submit", "csrf_token"]}
        user = User.query.filter_by(email=form_data["email"]).first()
        if user:
            if check_password_hash(user.password, form_data["password"]):
                login_user(user)
                return redirect(url_for("get_all_posts"))
            else:
                flash("Password incorrect, please try again.")
                return redirect(url_for("login"))
        else:
            flash("That email does not exist, please try again.")
            return redirect(url_for("login"))
    return render_template("login.html", form=form)


